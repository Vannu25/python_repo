# Dictionaries - unordered key value pairs


dict1 = {
    'a': 1,
    'b': 2,
    'c': 3
}
print(dict1['b'])

dict2 = [
    {
        'a': [4, 5],
        'b': 2,
        'c': 3
    },
    {
        'a': [1, 2],
        'b': 2,
        'c': 3
    }
]

print(dict2[0]['a'][1])

# dict methods

dict5 = {
    'basket': [1, 2, 3],
    'greet': "hello"
}

print(dict5['basket'])
